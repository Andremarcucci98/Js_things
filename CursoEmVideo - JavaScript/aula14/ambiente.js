// Repetição usando while
var c = 1
while (c<=5) {
    console.log(c)
    c++
}

// Mesmo código usando for
/*
Assim como em Python, para usar "for" é necessário saber a quantidade de elementos para executar sua lógica, 
portanto é melhor usar "for" para elementos finitos e "while" normalmente para elementos infinitos ou indefinidos.
*/

for (var c = 1;c <= 5; c++){
    console.log(c)
}


